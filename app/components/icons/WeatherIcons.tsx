export const getWeatherIcon = (condition: string) => {
  switch (condition.toLowerCase()) {
    case 'clear':
    case 'sunny':
      return <SunnyIcon />;
    case 'clouds':
    case 'cloudy':
    case 'partly cloudy':
      return <CloudyIcon />;
    case 'rain':
    case 'rainy':
    case 'shower rain':
      return <RainyIcon />;
    case 'snow':
      return <SnowIcon />;
    case 'thunderstorm':
      return <ThunderstormIcon />;
    case 'mist':
    case 'fog':
      return <MistIcon />;
    default:
      return <CloudyIcon />;
  }
}; 